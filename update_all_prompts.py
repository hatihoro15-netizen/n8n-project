def download_workflow(wf_id):
    result = subprocess.run([
        'curl', '-sk', f'{BASE}/workflows/{wf_id}',
        '-H', f'X-N8N-API-KEY: {API_KEY}'
    ], capture_output=True, text=True)
    return json.loads(result.stdout)


def upload_workflow(wf_id, wf_data):
    clean_wf = {
        "name": wf_data["name"],
        "nodes": wf_data["nodes"],
        "connections": wf_data["connections"],
        "settings": {"executionOrder": "v1"}
    }
    result = subprocess.run([
        'curl', '-sk', '-X', 'PUT',
        f'{BASE}/workflows/{wf_id}',
        '-H', f'X-N8N-API-KEY: {API_KEY}',
        '-H', 'Content-Type: application/json',
        '-d', json.dumps(clean_wf, ensure_ascii=False)
    ], capture_output=True, text=True)
    return json.loads(result.stdout)


